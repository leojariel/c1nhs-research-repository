        <h4>ABSTRACT:</h4>
        <div class="paragraphs">
         <p>The purpose of this study is to analyze the effects of modular distance learning and in-person classes on students' social interaction, the perceive effects in social interaction encountered during modular distance learning, the perceive effects in social interaction of students during in-person classes, the perceive effects of modular distance learning in transition to in-person classes in terms behavior, social and culture. </p>
         <p>The study expects to find no significant difference between modular distance learning and in-person classes on students' social interaction thus will use T-test to determine the difference between the two (2) variables. The data were gathered using survey questionnaire which are distributed to the selected 88 grade 11 students from Cagsiay 1 National High School, thus the respondents were selected using the sub variant of probability sampling which is stratified random sampling. Descriptive T-test were used to analyze the accumulated data from 88 selected respondents.</p>
         <p>The results revealed they there are significant difference between modular distance learning and in-person classes. In conclusion students interact more during in-person classes than modular distance learning wherein teachers should encourage the students to socially interact more during modular distance learning and they interact effectively during in-person classes.</p>
